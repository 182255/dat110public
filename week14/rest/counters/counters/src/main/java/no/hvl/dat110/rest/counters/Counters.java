package no.hvl.dat110.rest.counters;

public class Counters {
	
	private int red,green;

	public Counters () {
		this.red = 0;
		this.green = 0;
	}

	public int getRed() {
		return red;
	}

	public void setRed(int red) {
		this.red = red;
	}

	public int getGreen() {
		return green;
	}

	public void setGreen(int green) {
		this.green = green;
	}
	
}
